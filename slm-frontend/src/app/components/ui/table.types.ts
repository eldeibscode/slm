export type ColumnType = 'text' | 'date' | 'badge' | 'custom';
export type SortOrder = 'asc' | 'desc';

export interface TableColumn<T> {
  key: string;
  label: string;
  type: ColumnType;
  sortable?: boolean;
  filterable?: boolean;
  width?: string;
  badgeVariant?: (item: T) => 'default' | 'primary' | 'secondary' | 'success' | 'warning' | 'error';
  dateFormat?: string;
  customTemplate?: TemplateRef<any>;
  valueFormatter?: (item: T) => string;
}

export interface TableAction<T> {
  label: string;
  icon?: string | ((item: T) => string); // Can be static string or dynamic function
  iconOnly?: boolean; // Show only icon without label
  color?: string;
  visible?: (item: T) => boolean;
  handler: (item: T) => void;
  tooltip?: string | ((item: T) => string); // Can be static string or dynamic function
}

export interface StatusFilter {
  value: string;
  label: string;
  color?: string;
  count?: number;
}

export interface TableConfig<T> {
  columns: TableColumn<T>[];
  actions: TableAction<T>[];
  statusFilters?: StatusFilter[];
  searchPlaceholder?: string;
  searchKeys?: (keyof T)[];
  showStats?: boolean;
  emptyMessage?: string;
  showFilters?: boolean; // Show filters bar (default: true)
  showPagination?: boolean; // Show pagination (default: false)
  pageSize?: number; // Items per page (default: 10)
  hideSearchBar?: boolean; // Hide search bar completely (default: false)
}

export interface TableStats {
  total: number;
  [key: string]: number;
}
